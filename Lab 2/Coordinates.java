public class Coordinates {
 int x;
 int y;
 
 public Coordinates(int x, int y){
  this.x = x;
  this.y = y;
 }
 
 public int getX(){
  return x;
 }
 
 public int getY(){
  return y;
 }
 
 public void setX(int var){
  x = var;
 }
 
 public void setY(int var){
  y = var;
 }
}
