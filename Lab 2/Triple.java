
public class Triple {
	int x;
	int y;
	int z;
	
	public Triple(int x, int y, int z){
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public int getX(){
		return x;
	}
	
	public int getY(){
		return y;
	}
	
	public int getZ(){
		return z;
	}
	
	public void setX(int var){
		x = var;
	}
	
	public void setY(int var){
		y = var;
	}
	
	public void setZ(int var){
		z = var;
	}
}
